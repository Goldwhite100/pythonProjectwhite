# Arithmetic operators

num1 = 10
num2 = 3
print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)
print(num1 % num2) # Modulus - Returns the remainder after division

# Comparison operators - compare values
print(num1 < num2)   #true or false
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2)
print(num1 != num2)

# Assignment operators - Assign values to variables
x = 100
print(x)

x += 2
print(x)

y = 56
x -= 3
print(y)

# Logic operators
a = 23
print(a < 40 and a < 2)
print(a < 40 or a < 2)
print(not(a < 40 and a < 2))

#Operator precedence - Order in which operations get executed
result = (10+6/2-4)
print(result)
print(int(result))
